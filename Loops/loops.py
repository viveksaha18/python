# For Loop

for i in range(5):
    print("For Loop Iteration", i)


# While Loop
i = 1
while i <= 5:
    print("While Loop Iteration", i)
    i += 1

# Nested Loop
for i in range(3):
    for j in range(2):
        print("Nested Loop Iteration", i, j)


list = [10, 20, 30, 40, 50]
for item in list:
    print("List Item:", item)

